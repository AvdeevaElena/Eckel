package Chapter2;
//Exercise 13: (1) Write a method that displays char values in binary form. Demonstrate it using several different characters.

public class Task13 {
    public static void main(String[] args) {

        char ca ='a';
        char cc ='1';
        char cs ='%';
        System.out.println(Integer.toBinaryString(ca));
        System.out.println(Integer.toBinaryString(cc));
        System.out.println(Integer.toBinaryString(cs));

    }
}
